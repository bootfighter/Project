package com.mygdx.codeAssets.Animation;

public class AnimationState {
	public int millisecondsFromStart;
	public float rotation;
	public float scaleX;
	public float scaleY;
	
	public AnimationState(int a_millisecondsFromStart, float a_rotation, float a_scaleX, float a_scaleY){
		millisecondsFromStart = a_millisecondsFromStart;
		rotation = a_rotation;
		scaleX = a_scaleX;
		scaleY = a_scaleY;
	}
	

	
}
